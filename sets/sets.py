# Em Python, sets (ou conjuntos) são coleções não ordenadas de elementos únicos e imutáveis. Eles são úteis para armazenar itens sem duplicatas e para realizar operações matemáticas de conjuntos, como união, interseção e diferença.

numeros = [1, 2, 2, 3, 4, 4, 5]
meu_set = set(numeros)

