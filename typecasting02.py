#Typecasting is the process of converting a variable from one data type to another

name = "Vinícius"
age = 20
height = 1.70
is_worker = True

print(type(name))
print(type(age))
print(type(height))
print(type(is_worker))

height = int(height)
print(height)

age = float(age)
print(age)

name = bool(name)
print(name)
print(type(name))

is_worker = str(is_worker)
print(is_worker)
print(type(is_worker))
 