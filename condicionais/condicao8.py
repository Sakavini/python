numero = int(input("Informe um número para ver o dia da semana (1 a 7): "))

if numero == 1:
    print("Hoje é domingo")
elif numero == 2:
    print("Hoje é segunda feira")
elif numero == 3:
    print("Hoje é terça feira")
elif numero == 4:
    print("Hoje é quarta feira")
elif numero == 5:
    print("Hoje é quinta feira")
elif numero == 6:
    print("Hoje é sexta feira")
elif numero == 7:
    print("Hoje é sabado")
else:
    print("Número inválido")