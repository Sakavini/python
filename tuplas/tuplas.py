# Tuplas em Python são estruturas de dados muito parecidas com listas, mas imutáveis. Ou seja, depois de criadas, seus valores não podem ser alterados. São definidas usando parênteses ().

# Tuplas servem para armazenar coleções de dados que não devem ser alteradas. Elas são úteis quando você quer garantir que os valores permaneçam os mesmos durante a execução do programa.

numeros = (1, 5, 10, 15, 20)

print(max(numeros))
print(min(numeros))