primeiro_numero = float(input("Digite o primeiro número: "))
segundo_numero = float(input("Digite o segundo número: "))
operador = input("Escolha o operador desejado (+ - * /): ")

if operador == "+":
    resultado = primeiro_numero + segundo_numero
    print(resultado)
elif operador == "-":
    resultado = primeiro_numero - segundo_numero
    print(resultado)
elif operador == "*":
    resultado = primeiro_numero * segundo_numero
    print(resultado)
elif operador == "/":
    resultado = primeiro_numero / segundo_numero
    print(resultado)





