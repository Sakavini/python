# Loops são estruturas de repetição em programação. Eles servem para executar um bloco de código várias vezes, de acordo com uma condição ou sequência.

# Em Python, os principais tipos de loops são:

# for: usado para percorrer sequências (listas, strings, etc.).
# while: repete enquanto uma condição for verdadeira.


for i in range(1, 11):
    print(f"Loop: {i}")